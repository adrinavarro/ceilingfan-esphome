def test_ensure_web_password_appends_exactly_once(tmp_path) -> None:
    path = tmp_path / "secrets.yaml"
    with pytest.raises(CeilingFanError, match="secrets not found"):
        ensure_web_password(path)

    path.write_text('api_encryption_key: "value"\n', encoding="utf-8")
    ensure_web_password(path)
    first = path.read_text(encoding="utf-8")
    ensure_web_password(path)

    assert "web_password:" in first
    assert path.read_text(encoding="utf-8") == first


