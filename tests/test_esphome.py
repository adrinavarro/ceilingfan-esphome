def test_label_entity_warnings_flag_configurations_that_create_no_entity() -> None:
    assert label_entity_warnings(["fan_off", "fan_speed_1", "fan_speed_2"]) == []

    speed_without_off = label_entity_warnings(["fan_speed_1", "fan_speed_2"])
    assert any("fan_off" in warning for warning in speed_without_off)

    typo = label_entity_warnings(["fan_speed1", "fan_off"])
    assert any("typo" in warning and "fan_speed_1" in warning for warning in typo)

    light_on_only = label_entity_warnings(["light_on"])
    assert any("light_off" in warning for warning in light_on_only)

    broken_brightness = label_entity_warnings(
        ["light_off", "light_brightness_1", "light_brightness_3"]
    )
    assert any("contiguous" in warning for warning in broken_brightness)

    sparse_speeds = label_entity_warnings(["fan_off", "fan_speed_1", "fan_speed_3"])
    assert any("not contiguous" in warning for warning in sparse_speeds)


def test_validation_steps_cover_every_generated_entity() -> None:
    bedroom = DeviceProfile(
        name="Bedroom ceiling fan",
        frequency_hz=433_920_000,
        commands={
            "fan_off": waveform(),
            "fan_speed_1": waveform(),
            "fan_speed_2": waveform(),
            "light_off": waveform(),
            "light_brightness_1": waveform(),
            "light_brightness_2": waveform(),
            "fan_toggle": waveform(),
        },
    )
    office = build_cjoy_profile("Office CJOY", 0x175D0310)
    profiles = [bedroom, office]

    rendered = render_firmware(profiles, bridge_name="Home RF bridge")
    steps = validation_steps(profiles)

    # ESPHome project names are always author.project, so the dot filters the
    # discovery metadata out of the entity-name matches.
    entity_names = [
        name
        for name in re.findall(r"^    name: (.+)$", rendered, re.MULTILINE)
        if "." not in name
    ]
    rendered_object_ids = {_identifier(name) for name in entity_names}
    assert rendered_object_ids == {step.object_id for step in steps}

    validated_commands = {(step.profile_name, step.command) for step in steps}
    for profile in profiles:
        for command in profile.command_names():
            assert (profile.name, command) in validated_commands


def test_validation_steps_send_absolute_states_and_synchronize_cjoy_first() -> None:
    bedroom = DeviceProfile(
        name="Bedroom ceiling fan",
        frequency_hz=433_920_000,
        commands={
            "fan_off": waveform(),
            "fan_speed_1": waveform(),
            "light_off": waveform(),
            "light_brightness_1": waveform(),
            "light_brightness_2": waveform(),
        },
    )
    steps = validation_steps([bedroom])
    by_command = {step.command: step for step in steps}
    assert by_command["fan_speed_1"].state is True
    assert by_command["fan_speed_1"].speed == 1
    assert by_command["fan_off"].state is False
    assert by_command["light_brightness_1"].brightness == 0.5
    assert by_command["light_brightness_2"].brightness == 1.0
    assert by_command["fan_off"].object_id == "ceiling_fan"
    assert by_command["light_off"].object_id == "ceiling_fan_light"

    cjoy_steps = validation_steps([build_cjoy_profile("Office CJOY", 0x175D0310)])
    assert cjoy_steps[0].command == "synchronize_rf_phase"
    assert cjoy_steps[0].entity_type == "button"
    assert cjoy_steps[0].object_id == "cjoy_synchronize_rf_phase"


